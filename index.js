'use strict';

module.exports = function isOdd(value) {
  const n = Math.abs(value);
  if (!Number.isInteger(n)) {
    throw new Error('expected an integer');
  }
  if (!Number.isSafeInteger(n)) {
    throw new Error('value exceeds maximum safe integer');
  }
  return (n & 1) === 1;
};
